---@param is_img boolean
---@param number? integer
function M.setup(is_img, number)
  is_img = is_img or false
  number = number or 0

  local __tbl = is_img and images or logos

  math.randomseed(os.time())
  local check = math.random(1, #__tbl)

  if number > 0 then
    if __tbl[number] then
      return __tbl[number]
    end
  end

  return __tbl[check]
end

return M
